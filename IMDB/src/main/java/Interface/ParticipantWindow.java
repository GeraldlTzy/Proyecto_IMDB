    public void loadData() {
        cmbNationalities.removeAllItems();
        cmbProvinces.removeAllItems();
        try {
            data = controller.getInfoRegisterParticipant(null);
            nationalities = (TreeMap) data.get(0);
            cities = (LinkedHashMap)data.get(1);
        } catch (SQLException ex) {
            System.out.println("Ventana de error");
        }
        for (Map.Entry<Integer, String> entry : nationalities.entrySet()) {
            cmbNationalities.addItem(entry.getValue());
        }
        for (Map.Entry<Integer, String> entry : cities.entrySet()) {
            cmbProvinces.addItem(entry.getValue());
        }
    }
